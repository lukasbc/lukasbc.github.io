    <h1>
      Présentation du jeu et contexte
    </h1>
    <p>
      Pendant ma licence, j'ai participé à un projet de groupe où nous devions créer un jeu vidéo avec d'autres étudiants de la formation, nous l'avons appelé Ludoie.
    </p>
    <p>
      Ludoie combine les éléments classiques du jeu de société traditionnel Petits
      Chevaux avec les mécaniques du jeu de l'oie. Les joueurs déplacent leurs pions sur un plateau
      3D en suivant les règles familières du jeu. Avec des rebondissements imprévisibles, des choix
      tactiques et la possibilité d'interagir avec les pions des autres joueurs.
    </p>


    <h1>
      Équipe et organisation
    </h1>

    <p>
      Nous étions une équipe de 12, chacun ayant ses rôles respectifs. Pour ma part, j'étais dans l'équipe d'IHM et de la communication du projet.
    </p>

    <p>
      Nous organisions plusieurs réunions hebdomadaires tout au long du projet, avec que des versions alpha, bêta et finale, ainsi qu'une soutenance à la fin du projet accompagnée d'un rapport.
    </p>


    <h1>
      Technologies et outils utilisés
    </h1>

    <p>
      Pour le coté IHM je me suis servi de <b>Photoshop</b> principalement pour l'identité visuelle, de <b>FL Studio</b> pour l'identité sonore ainsi que de <b>Blender</b> pour la manipulation des pions et <b>Figma</b> pour créer les wireframes.
    </p>

    <p>
      Pour ce qui est de la communication je me suis servi de <b>Photoshop</b> pour les montages photos et de <b>Adobe Premiere Pro</b> pour les montages vidéos.</p>

