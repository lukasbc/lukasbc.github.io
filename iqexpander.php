    <h2>
      C'est quoi Iq Expander?
    </h2>
    <p>Iq Expander est un jeu video sorti en 1997 par Ravensburger Interactive. Il s'agit d'un jeu de puzzle de type point-and-click, principalement basé sur la mémoire.</p>
    <p>Il comporte 15 mini-jeux différents, chacun avec 5 niveaux de difficulté, et jouables jusqu'à 4 joueurs.</p>

    <h2>
    Pourquoi l'avoir "restauré"
    </h2>
    <p>Malheureusement, ce jeu rencontre des problèmes de compatibilité avec les systèmes et environnements récents (au dela de Windows 95).</p>
    <p>Ce jeu a marqué l'enfance de moi et mes freres, surtout par sa musique et son design. Un jour un de mes freres a evoqué l'idée de faire notre propre version du jeu avec 6 joueurs, c'est ce qui m'a poussé à me lancer dans la création d'une version restaurée de ce jeu.</p>


    <h2>
    Mon objectif
    </h2>
    <p>Rendre le jeu fonctionnel en y ajoutant des corrections et des améliorations de qualité de vie, tout en respectant au maximum la version originale.</p>
    
    <h2>
    Comment je m'y suis pris?
    </h2>
    <p>
    J'ai d'abord enregistré du gameplay du jeu dans une machine virtuelle à l'aide d'OBS, afin d'analyser précisément son fonctionnement d'origine.
    </p>
    <p>
    J'ai ensuite extrait tous les sprites, musiques et effets sonores avec l'aide de <b>Adobe Director 11.5</b>, <b>Photoshop</b> et <b>FL Studio</b>.
    </p>
    <p>
    Puis je me suis mis a coder sur <b>GameMaker Studio 2</b> en me fiant au gameplay enregistré plus tot et en m'aidant de divers sites et forums.
    </p>


    <h2>
    Quels sont les améliorations?
    </h2>

    <li>Passage de 4 a 6 joueurs maximum</li>
    <li>Informations sur quoi utiliser pour les jeux (souris et/ou clavier)</li>
    <li>Ajout de personnages, d'effet sonores et musiques manquant au jeu original mais trouvé dans les fichiers</li>
    <li>Ajout de fonctionnalités d'accessibilité, comme la possibilité de jouer au clavier et non uniquement à la souris</li>



